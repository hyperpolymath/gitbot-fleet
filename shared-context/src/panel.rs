//! Panel lifecycle types for eNSAID-aware bot coordination.
//!
//! This module gives the gitbot-fleet awareness of PanLL panels — their
//! structure, lifecycle phases, wiring requirements, and validation rules.
//! Bots use these types to produce panel-specific findings during minting,
//! provisioning, configuration, and wiring.
//!
//! ## Panel Lifecycle
//!
//! A panel moves through phases:
//!
//! ```text
//! Minted → Wired → Configured → Provisioned → Active
//!   ↑         ↑         ↑            ↑           ↑
//! rhodibot  seambot  echidnabot  finishbot    runtime
//! glambot             seambot
//! ```
//!
//! Each phase has validation rules enforced by specific bots.

use serde::{Deserialize, Serialize};
use std::path::PathBuf;

// =============================================================================
// Panel Identity
// =============================================================================

/// Unique panel identifier — PascalCase name matching the ReScript variant.
///
/// Example: "CloudGuard", "ValenceShell", "Minter"
#[derive(Debug, Clone, PartialEq, Eq, Hash, Serialize, Deserialize)]
pub struct PanelId(pub String);

impl PanelId {
    /// Create a new panel ID, validating PascalCase format.
    pub fn new(name: &str) -> Result<Self, PanelError> {
        if name.is_empty() {
            return Err(PanelError::InvalidName("Panel name cannot be empty".into()));
        }
        if !name.chars().next().map(|c| c.is_uppercase()).unwrap_or(false) {
            return Err(PanelError::InvalidName(
                "Panel name must start with uppercase (PascalCase)".into(),
            ));
        }
        if !name.chars().all(|c| c.is_alphanumeric()) {
            return Err(PanelError::InvalidName(
                "Panel name must be alphanumeric only".into(),
            ));
        }
        if RESERVED_NAMES.contains(&name) {
            return Err(PanelError::ReservedName(name.into()));
        }
        Ok(Self(name.to_string()))
    }

    /// Get the camelCase form (e.g. "cloudGuard").
    pub fn to_camel_case(&self) -> String {
        if self.0.is_empty() {
            return String::new();
        }
        let mut chars = self.0.chars();
        let first = chars.next().unwrap_or_default().to_lowercase().to_string();
        format!("{}{}", first, chars.collect::<String>())
    }

    /// Get the snake_case form (e.g. "cloud_guard").
    pub fn to_snake_case(&self) -> String {
        let mut result = String::new();
        for (i, ch) in self.0.chars().enumerate() {
            if ch.is_uppercase() {
                if i > 0 {
                    result.push('_');
                }
                result.extend(ch.to_lowercase());
            } else {
                result.push(ch);
            }
        }
        result
    }

    /// Get the raw PascalCase name.
    pub fn name(&self) -> &str {
        &self.0
    }
}

impl std::fmt::Display for PanelId {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        write!(f, "{}", self.0)
    }
}

/// Names that cannot be used for panels — they collide with PanLL internals.
pub const RESERVED_NAMES: &[&str] = &[
    "Model", "View", "Update", "Msg", "App", "Main", "Tea", "Panel", "Pane",
    "PaneL", "PaneN", "PaneW", "PanelSwitcher", "Storage", "Connection",
];

