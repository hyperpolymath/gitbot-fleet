// Panel Manifest (what a panel IS)
// =============================================================================

/// Complete description of a panel and its current lifecycle state.
///
/// This is the primary data structure that bots examine when validating
/// a panel. It captures both the intended structure (from the minting
/// request) and the actual state (from filesystem inspection).
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct PanelManifest {
    /// Panel identity (PascalCase name).
    pub id: PanelId,
    /// Short name for the panel bar (max ~8 chars).
    pub short_name: String,
    /// One-line description.
    pub description: String,
    /// Icon identifier for the panel bar.
    pub icon: String,
    /// Whether this panel has a Rust/Tauri backend.
    pub has_backend: bool,
    /// Current lifecycle phase.
    pub phase: PanelPhase,
    /// Clade identifier (e.g. "scanner", "builder", "viewer").
    pub clade_id: Option<String>,
    /// Isolation tier assigned by the Provisioner.
    pub isolation: Option<IsolationTier>,
    /// Results of structural wiring checks.
    pub wiring: WiringStatus,
    /// Bot validation results (most recent per bot).
    pub validations: Vec<PanelValidation>,
}

impl PanelManifest {
    /// Create a manifest for a newly minted panel.
    pub fn new_minted(
        id: PanelId,
        short_name: &str,
        description: &str,
        icon: &str,
        has_backend: bool,
    ) -> Self {
        Self {
            id,
            short_name: short_name.to_string(),
            description: description.to_string(),
            icon: icon.to_string(),
            has_backend,
            phase: PanelPhase::Minted,
            clade_id: None,
            isolation: None,
            wiring: WiringStatus::default(),
            validations: Vec::new(),
        }
    }

    /// Check whether all wiring points are connected.
    pub fn is_fully_wired(&self) -> bool {
        self.wiring.is_complete()
    }

    /// Check whether the panel can advance to the next phase.
    pub fn can_advance(&self) -> bool {
        match self.phase {
            PanelPhase::Minted => self.wiring.is_complete(),
            PanelPhase::Wired => self.clade_id.is_some() && self.isolation.is_some(),
            PanelPhase::Configured => {
                // No Error findings from any bot
                !self.validations.iter().any(|v| v.has_errors)
            }
            PanelPhase::Provisioned => true, // Activation is a runtime decision
            PanelPhase::Active => false,      // Terminal phase
        }
    }

    /// Advance the panel to the next lifecycle phase.
    /// Returns Err if advancement conditions are not met.
    pub fn advance(&mut self) -> Result<PanelPhase, PanelError> {
        if !self.can_advance() {
            return Err(PanelError::CannotAdvance {
                panel: self.id.clone(),
                phase: self.phase,
            });
        }
        self.phase = match self.phase {
            PanelPhase::Minted => PanelPhase::Wired,
            PanelPhase::Wired => PanelPhase::Configured,
            PanelPhase::Configured => PanelPhase::Provisioned,
            PanelPhase::Provisioned => PanelPhase::Active,
            PanelPhase::Active => return Err(PanelError::AlreadyActive(self.id.clone())),
        };
        Ok(self.phase)
    }
}

