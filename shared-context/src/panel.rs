/// The expected file structure for a panel, following DD-004's 8-file pattern.
///
/// Used by rhodibot to verify that all required files exist after minting.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct PanelFileExpectation {
    /// Human description of this file's role.
    pub role: String,
    /// Expected path relative to repo root.
    pub path: PathBuf,
    /// Whether this file is required (vs optional).
    pub required: bool,
    /// Whether this file was found.
    pub found: bool,
}

impl PanelFileExpectation {
    /// Generate the expected file list for a panel.
    pub fn expected_files(panel: &PanelId, has_backend: bool) -> Vec<Self> {
        let name = panel.name();
        let snake = panel.to_snake_case();

        let mut files = vec![
            Self {
                role: "Model types".into(),
                path: PathBuf::from(format!("src/model/{}Model.res", name)),
                required: true,
                found: false,
            },
            Self {
                role: "Engine (pure computation)".into(),
                path: PathBuf::from(format!("src/core/{}Engine.res", name)),
                required: true,
                found: false,
            },
            Self {
                role: "Component (view)".into(),
                path: PathBuf::from(format!("src/components/{}.res", name)),
                required: true,
                found: false,
            },
            Self {
                role: "Module registration".into(),
                path: PathBuf::from(format!("src/modules/{}Module.res", name)),
                required: true,
                found: false,
            },
            Self {
                role: "Clade manifest".into(),
                path: PathBuf::from(format!(
                    "panel-clades/clades/{}/{}.a2ml",
                    panel.to_camel_case(),
                    name
                )),
                required: false, // Generated post-mint
                found: false,
            },
            Self {
                role: "Engine tests".into(),
                path: PathBuf::from(format!(
                    "tests/{}_engine_test.js",
                    snake
                )),
                required: false, // Checked by finishbot, not rhodibot
                found: false,
            },
        ];

        if has_backend {
            files.extend([
                Self {
                    role: "Commands (Tauri bridge)".into(),
                    path: PathBuf::from(format!("src/commands/{}Cmd.res", name)),
                    required: true,
                    found: false,
                },
                Self {
                    role: "Rust module".into(),
                    path: PathBuf::from(format!("src-tauri/src/{}/mod.rs", snake)),
                    required: true,
                    found: false,
                },
                Self {
                    role: "Rust types".into(),
                    path: PathBuf::from(format!("src-tauri/src/{}/types.rs", snake)),
                    required: true,
                    found: false,
                },
                Self {
                    role: "Rust commands".into(),
                    path: PathBuf::from(format!("src-tauri/src/{}/commands.rs", snake)),
                    required: true,
                    found: false,
                },
            ]);
        }

        files
    }

    /// Check which files actually exist on disk.
    pub fn check_existence(files: &mut [Self], repo_root: &std::path::Path) {
        for file in files.iter_mut() {
            file.found = repo_root.join(&file.path).exists();
        }
    }

    /// Get files that are required but missing.
    pub fn missing_required(files: &[Self]) -> Vec<&Self> {
        files
            .iter()
            .filter(|f| f.required && !f.found)
            .collect()
    }
}

