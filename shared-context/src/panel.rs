/// Errors specific to panel lifecycle operations.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub enum PanelError {
    /// Panel name is invalid (not PascalCase, empty, etc.).
    InvalidName(String),
    /// Panel name is reserved by PanLL internals.
    ReservedName(String),
    /// Panel cannot advance to the next phase (conditions not met).
    CannotAdvance {
        panel: PanelId,
        phase: PanelPhase,
    },
    /// Panel is already in the Active phase.
    AlreadyActive(PanelId),
    /// Panel not found in context.
    NotFound(PanelId),
}

impl std::fmt::Display for PanelError {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            PanelError::InvalidName(msg) => write!(f, "Invalid panel name: {}", msg),
            PanelError::ReservedName(name) => write!(f, "'{}' is a reserved panel name", name),
            PanelError::CannotAdvance { panel, phase } => {
                write!(f, "Panel '{}' cannot advance from {} phase", panel, phase)
            }
            PanelError::AlreadyActive(panel) => {
                write!(f, "Panel '{}' is already active", panel)
            }
            PanelError::NotFound(panel) => {
                write!(f, "Panel '{}' not found in context", panel)
            }
        }
    }
}

