/// Result of a bot's validation of a panel.
///
/// Each bot produces one of these after checking a panel. The panel
/// manifest accumulates these to track overall readiness.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct PanelValidation {
    /// Which bot performed the validation.
    pub bot: crate::bot::BotId,
    /// When the validation was performed.
    pub validated_at: chrono::DateTime<chrono::Utc>,
    /// Whether the validation passed (no errors).
    pub passed: bool,
    /// Whether there were Error-severity findings.
    pub has_errors: bool,
    /// Number of findings at each severity.
    pub error_count: usize,
    pub warning_count: usize,
    pub info_count: usize,
    /// Summary message.
    pub summary: String,
}

