/// The lifecycle phase of a panel within the eNSAID environment.
///
/// Panels progress through phases as bots validate them. Each phase
/// has specific requirements that must be met before advancing.
#[derive(Debug, Clone, Copy, PartialEq, Eq, PartialOrd, Ord, Hash, Serialize, Deserialize)]
#[serde(rename_all = "kebab-case")]
pub enum PanelPhase {
    /// Panel files have been generated by the Minter.
    /// Requirements: 4+ ReScript files exist (Model, Engine, Component, Module).
    /// Validated by: rhodibot (structure), glambot (presentation).
    Minted,

    /// Panel is wired into the global TEA loop.
    /// Requirements: PanelSwitcherModel variant, PanelRegistry entry,
    /// Model.res include, Msg.res type + variant, View.res case, Update.res handler.
    /// Validated by: seambot (seam integrity).
    Wired,

    /// Panel is configured with isolation tier, BoJ routing, and clade assignment.
    /// Requirements: Provisioner config entry, isolation tier set, clade A2ML manifest.
    /// Validated by: echidnabot (type safety), seambot (integration contracts).
    Configured,

    /// Panel has passed all release-readiness checks.
    /// Requirements: Tests exist and pass, clade registered, no Error-severity
    /// findings from any bot, documentation present.
    /// Validated by: finishbot (release gate).
    Provisioned,

    /// Panel is running in the eNSAID environment.
    /// This phase is set at runtime, not by bots.
    Active,
}

impl PanelPhase {
    /// Human-readable description of this phase.
    pub fn description(&self) -> &'static str {
        match self {
            PanelPhase::Minted => "Files generated, awaiting wiring",
            PanelPhase::Wired => "TEA loop connected, awaiting configuration",
            PanelPhase::Configured => "Isolation and clade set, awaiting release gate",
            PanelPhase::Provisioned => "Release-ready, awaiting activation",
            PanelPhase::Active => "Running in eNSAID environment",
        }
    }
}

impl std::fmt::Display for PanelPhase {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            PanelPhase::Minted => write!(f, "minted"),
            PanelPhase::Wired => write!(f, "wired"),
            PanelPhase::Configured => write!(f, "configured"),
            PanelPhase::Provisioned => write!(f, "provisioned"),
            PanelPhase::Active => write!(f, "active"),
        }
    }
}

