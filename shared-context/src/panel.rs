        ctx.register_panel(manifest);

        let retrieved = ctx.get_panel(&id).unwrap();
        assert_eq!(retrieved.id, id);
        assert_eq!(retrieved.phase, PanelPhase::Minted);
        assert!(!retrieved.is_fully_wired());

