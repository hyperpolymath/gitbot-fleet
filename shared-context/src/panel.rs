/// Extension trait for Context to support panel lifecycle operations.
///
/// This allows bots to register panels, track their phases, and query
/// wiring status through the shared context's `data` HashMap.
pub trait PanelContext {
    /// Register a panel manifest in the shared context.
    fn register_panel(&mut self, manifest: PanelManifest);

    /// Get a panel manifest by ID.
    fn get_panel(&self, id: &PanelId) -> Option<PanelManifest>;

    /// Get all registered panel manifests.
    fn all_panels(&self) -> Vec<PanelManifest>;

    /// Update a panel's wiring status.
    fn update_panel_wiring(&mut self, id: &PanelId, wiring: WiringStatus)
        -> Result<(), PanelError>;

    /// Record a bot's validation result for a panel.
    fn record_panel_validation(
        &mut self,
        id: &PanelId,
        validation: PanelValidation,
    ) -> Result<(), PanelError>;

    /// Try to advance a panel to the next phase.
    fn advance_panel(&mut self, id: &PanelId) -> Result<PanelPhase, PanelError>;
}

impl PanelContext for crate::context::Context {
    fn register_panel(&mut self, manifest: PanelManifest) {
        let key = format!("panel:{}", manifest.id);
        if let Ok(value) = serde_json::to_value(&manifest) {
            self.data.insert(key, value);
        }
    }

    fn get_panel(&self, id: &PanelId) -> Option<PanelManifest> {
        let key = format!("panel:{}", id);
        self.data
            .get(&key)
            .and_then(|v| serde_json::from_value(v.clone()).ok())
    }

    fn all_panels(&self) -> Vec<PanelManifest> {
        self.data
            .iter()
            .filter(|(k, _)| k.starts_with("panel:"))
            .filter_map(|(_, v)| serde_json::from_value(v.clone()).ok())
            .collect()
    }

    fn update_panel_wiring(
        &mut self,
        id: &PanelId,
        wiring: WiringStatus,
    ) -> Result<(), PanelError> {
        let key = format!("panel:{}", id);
        let mut manifest: PanelManifest = self
            .data
            .get(&key)
            .and_then(|v| serde_json::from_value(v.clone()).ok())
            .ok_or_else(|| PanelError::NotFound(id.clone()))?;

        manifest.wiring = wiring;
        if let Ok(value) = serde_json::to_value(&manifest) {
            self.data.insert(key, value);
        }
        Ok(())
    }

    fn record_panel_validation(
        &mut self,
        id: &PanelId,
        validation: PanelValidation,
    ) -> Result<(), PanelError> {
        let key = format!("panel:{}", id);
        let mut manifest: PanelManifest = self
            .data
            .get(&key)
            .and_then(|v| serde_json::from_value(v.clone()).ok())
            .ok_or_else(|| PanelError::NotFound(id.clone()))?;

        // Replace any existing validation from the same bot
        manifest.validations.retain(|v| v.bot != validation.bot);
        manifest.validations.push(validation);

        if let Ok(value) = serde_json::to_value(&manifest) {
            self.data.insert(key, value);
        }
        Ok(())
    }

    fn advance_panel(&mut self, id: &PanelId) -> Result<PanelPhase, PanelError> {
        let key = format!("panel:{}", id);
        let mut manifest: PanelManifest = self
            .data
            .get(&key)
            .and_then(|v| serde_json::from_value(v.clone()).ok())
            .ok_or_else(|| PanelError::NotFound(id.clone()))?;

        let new_phase = manifest.advance()?;
        if let Ok(value) = serde_json::to_value(&manifest) {
            self.data.insert(key, value);
        }
        Ok(new_phase)
    }
}

