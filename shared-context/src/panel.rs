// Wiring Status (the 6 connection points)
// =============================================================================

/// Status of the 6 global wiring points that connect a panel to the TEA loop.
///
/// Each field tracks whether the corresponding insertion in the global file
/// has been detected. Seambot populates this by scanning the actual source files.
#[derive(Debug, Clone, Default, Serialize, Deserialize)]
pub struct WiringStatus {
    /// `src/model/PanelSwitcherModel.res` — `| PanelXxx` variant exists.
    pub panel_switcher_model: WiringCheck,
    /// `src/modules/PanelRegistry.res` — panel metadata entry exists.
    pub panel_registry: WiringCheck,
    /// `src/Model.res` — `include XxxModel` and state field exist.
    pub model_include: WiringCheck,
    /// `src/Msg.res` — message type and routing variant exist.
    pub msg_routing: WiringCheck,
    /// `src/View.res` — `renderActivePanel` case exists.
    pub view_dispatch: WiringCheck,
    /// `src/Update.res` — sub-updater function and routing case exist.
    pub update_handler: WiringCheck,
    /// `src-tauri/src/main.rs` — Rust commands registered (if has_backend).
    /// Set to `Connected` with note "no backend" for frontend-only panels.
    pub rust_registration: WiringCheck,
}

impl WiringStatus {
    /// Check whether all 7 wiring points are connected.
    pub fn is_complete(&self) -> bool {
        self.panel_switcher_model.is_connected()
            && self.panel_registry.is_connected()
            && self.model_include.is_connected()
            && self.msg_routing.is_connected()
            && self.view_dispatch.is_connected()
            && self.update_handler.is_connected()
            && self.rust_registration.is_connected()
    }

    /// Count how many wiring points are connected vs total.
    pub fn score(&self) -> (usize, usize) {
        let checks = [
            &self.panel_switcher_model,
            &self.panel_registry,
            &self.model_include,
            &self.msg_routing,
            &self.view_dispatch,
            &self.update_handler,
            &self.rust_registration,
        ];
        let connected = checks.iter().filter(|c| c.is_connected()).count();
        (connected, checks.len())
    }

    /// Get all disconnected wiring points with their descriptions.
    pub fn disconnected(&self) -> Vec<(&'static str, &'static str)> {
        let mut missing = Vec::new();
        if !self.panel_switcher_model.is_connected() {
            missing.push(("PanelSwitcherModel.res", "Panel ID variant"));
        }
        if !self.panel_registry.is_connected() {
            missing.push(("PanelRegistry.res", "Panel metadata entry"));
        }
        if !self.model_include.is_connected() {
            missing.push(("Model.res", "Include + state field"));
        }
        if !self.msg_routing.is_connected() {
            missing.push(("Msg.res", "Message type + routing variant"));
        }
        if !self.view_dispatch.is_connected() {
            missing.push(("View.res", "renderActivePanel case"));
        }
        if !self.update_handler.is_connected() {
            missing.push(("Update.res", "Sub-updater + routing case"));
        }
        if !self.rust_registration.is_connected() {
            missing.push(("main.rs", "Rust command registration"));
        }
        missing
    }
}

/// Status of a single wiring connection point.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct WiringCheck {
    /// Whether this wiring point is connected.
    pub status: WiringCheckStatus,
    /// File where this wiring point lives.
    pub file: Option<PathBuf>,
    /// Line number where the wiring was found (for diagnostics).
    pub line: Option<usize>,
    /// Optional note (e.g. "no backend needed").
    pub note: Option<String>,
}

impl Default for WiringCheck {
    fn default() -> Self {
        Self {
            status: WiringCheckStatus::Unknown,
            file: None,
            line: None,
            note: None,
        }
    }
}

impl WiringCheck {
    /// Create a connected check with file location.
    pub fn connected(file: PathBuf, line: usize) -> Self {
        Self {
            status: WiringCheckStatus::Connected,
            file: Some(file),
            line: Some(line),
            note: None,
        }
    }

    /// Create a connected check with a note (e.g. "no backend").
    pub fn connected_with_note(note: &str) -> Self {
        Self {
            status: WiringCheckStatus::Connected,
            file: None,
            line: None,
            note: Some(note.to_string()),
        }
    }

    /// Create a disconnected check.
    pub fn disconnected() -> Self {
        Self {
            status: WiringCheckStatus::Disconnected,
            file: None,
            line: None,
            note: None,
        }
    }

    /// Whether this check indicates a connected wiring point.
    pub fn is_connected(&self) -> bool {
        matches!(self.status, WiringCheckStatus::Connected)
    }
}

/// Status of a wiring check.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "lowercase")]
pub enum WiringCheckStatus {
    /// Not yet checked.
    Unknown,
    /// Wiring point is present and correct.
    Connected,
    /// Wiring point is missing or incorrect.
    Disconnected,
}

