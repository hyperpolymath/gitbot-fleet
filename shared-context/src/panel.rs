/// Panel isolation tier — determines security boundary.
///
/// From Design Decision DD-005: Three-Tier Panel Isolation.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "kebab-case")]
pub enum IsolationTier {
    /// In-process Tauri webview. Full trust, hash-verified. Core panels.
    Native,
    /// Alpine + Podman container. Process isolation, network limited.
    StandardPod,
    /// Stapeln + Chainguard image. Full security stack.
    HardenedPod,
}

impl IsolationTier {
    /// Human-readable label.
    pub fn label(&self) -> &'static str {
        match self {
            IsolationTier::Native => "Native (in-process)",
            IsolationTier::StandardPod => "Standard Pod (Podman)",
            IsolationTier::HardenedPod => "Hardened Pod (Stapeln + Chainguard)",
        }
    }
}

